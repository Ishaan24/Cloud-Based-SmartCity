      <h2>About Smart City</h2>
      <p><a href=""></a></p>
      <p>A smart city is an urban development vision to integrate multiple information and communication technology (ICT) solutions in a secure fashion to manage a city? assets ? the city? assets include, but not limited to, local departments information systems, schools, libraries, transportation systems, hospitals, power plants, law enforcement, and other community services.<br> <br>The goal of building a smart city is to improve quality of life by using technology to improve the efficiency of services and meet residents? needs. ICT allows city officials to interact directly with the community and the city infrastructure and to tell what is happening in the city, how the city is evolving, and how to enable a better quality of life.<br><br> Through the use of real-time systems and sensors, data are collected from citizens and objects - then processed in real-time<br>Smart City by Sam Musa ICT is used to enhance quality, performance and interactivity of urban services, to 
reduce costs and resource consumption and to improve contact between citizens and government.Smart city
applications are developed with the goal of improving the management of urban flows and allowing for 
real time responses to challenges. <br><br>A smart city may therefore be more prepared to respond to challenges
than one with a simple 'transactional' relationship with its citizens.<br>Other terms that have been used for similar concepts include cyberville, digital city, electronic
communities, flexicity, information city, intelligent city, knowledge-based city, MESH city, telecity,
teletopia, Ubiquitous city, wired city</p>
